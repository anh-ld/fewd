// PI, square root, logarit
console.log("PI: " + Math.PI.toFixed(2));
console.log("Square root of 2: " + Math.SQRT2.toFixed(2)); // square root of 2
console.log("Log of 10: " + Math.LN10.toFixed(2)); // log of 10

// random number
var randomNumber = Math.random().toFixed(2); // random from 0 - 1
var randomNumber2 = 20 * Math.random().toFixed(2); // random from 0 - 20

console.log(randomNumber);
console.log(randomNumber2);

// round, floor, ceil
var n1 = 0.35;
var n2 = 0.85;
// round
console.log(Math.round(n1));
console.log(Math.round(n2));
// floor
console.log(Math.floor(n1));
console.log(Math.floor(n2));
// ceil
console.log(Math.ceil(n1));
console.log(Math.ceil(n2));

// get min, max
console.log(Math.max(20, 59));
console.log(Math.min(20, 59));

